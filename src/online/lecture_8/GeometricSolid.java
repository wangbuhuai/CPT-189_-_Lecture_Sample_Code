package online.lecture_8;

public interface GeometricSolid {
    /** Calculates the volume of the geometric solid.
        @return: calculated volume of the geometric solid
    */
    double volume();

    /** Calculates the surface area of the geometric solid.
        @return: calculated surface area of the geometric solid
    */
    double surfaceArea();
}